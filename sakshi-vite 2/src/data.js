export function loadMaster() {
  try {
    const saved = JSON.parse(localStorage.getItem("sk_master_v2"))
    if (saved) return saved
  } catch {}
  const m = {}
  ;["breakfast","lunch","dinner","snacks"].forEach(cat => {
    m[cat] = MEAL_DB[cat].map(d => ({ name: d.name, protein: d.protein, tip: d.tip || "", bulk: d.bulk || "" }))
  })
  return m
}
