export function pickRandom(arr, n = 1) {
  const s = [...arr].sort(() => Math.random() - 0.5)
  return n === 1 ? s[0] : s.slice(0, n)
}

export function getTodayPlan(master) {
  const plan = {}
  ;["breakfast","lunch","dinner"].forEach((cat, ci) => {
    const pool = master[cat] || []
    const idx = (new Date().getDay() * 3 + ci * 7) % pool.length
    plan[cat] = pool[idx]
  })
  plan.snacks = (master.snacks || []).slice(0, 3)
  return plan
}

export function getFridgeSuggestions(veggies, master) {
  const result = {}
  if (!veggies.length) {
    const safe = {
      breakfast: ["Moong dal cheela + dahi","Besan chilla + green chutney","Sattu paratha + pickle","Soya granule cheela","Oats with milk + dry fruits"],
      lunch:     ["Rajma chawal + salad","Toor dal + rice + ghee + salad","Moong dal khichdi + dahi + ghee","Chana masala + rice + raita"],
      dinner:    ["Paneer bhurji + 2 chapatis","Dal tadka + roti + pickle","Soya chunks curry + roti","Chana dal + roti + raita"],
    }
    ;["breakfast","lunch","dinner"].forEach(cat => {
      const pool = (master[cat] || []).filter(d => safe[cat].includes(d.name))
      result[cat] = pool.length ? pickRandom(pool) : pickRandom(master[cat] || [])
    })
    return result
  }
  const scores = {}
  veggies.forEach(v => {
    ;(VEGGIE_RECIPES[v] || []).forEach(r => { scores[r] = (scores[r] || 0) + 1 })
  })
  const topNames = Object.entries(scores).sort((a,b) => b[1]-a[1]).map(([n]) => n)
  ;["breakfast","lunch","dinner"].forEach(cat => {
    const matched = (master[cat] || []).find(d => topNames.includes(d.name))
    result[cat] = matched || pickRandom(master[cat] || [])
  })
  return result
}
